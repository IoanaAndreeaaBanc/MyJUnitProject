public class Calculator {
    public int add(int x, int y) {
        return x + y;
    }

    public int scadere(int x, int y){
        return x-y;

    }
    public int inmultire(int x, int y){
        return x*y;

    }
    public int impartire(int x, int y){
        return x/y;
    }

    public int calculeazaRestulImpartirii(int x, int y){
        return x%y;
    }



}
